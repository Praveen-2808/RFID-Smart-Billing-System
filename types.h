/*
 * types.h
 * -----------------------------------------------------------------------
 * Short, fixed-width style type aliases used throughout the embedded
 * project (microcontroller side). These give consistent, easy-to-read
 * names instead of writing "unsigned char", "signed short int", etc.
 * everywhere.
 * -----------------------------------------------------------------------
 */

typedef unsigned char u8;          /* 8-bit  unsigned integer */
typedef char s8;                   /* 8-bit  signed integer / character */
typedef	unsigned int u32;          /* 32-bit unsigned integer */
typedef	int s32;                   /* 32-bit signed integer */
typedef	unsigned short int u16;    /* 16-bit unsigned integer */
typedef	signed short int s16;      /* 16-bit signed integer */
typedef	float f32;                 /* 32-bit floating point */
typedef	double d64;                /* 64-bit floating point */

